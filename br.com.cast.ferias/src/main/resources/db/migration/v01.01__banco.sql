CREATE TABLE EQUIPE(
    IDEQUIPE BIGINT AUTO_INCREMENT,
    NMEQUIPE VARCHAR(100) NOT NULL,
 CONSTRAINT PK_EQUIPE PRIMARY KEY(IDEQUIPE));
)

CREATE TABLE FUNCIONARIO(
    IDFUNCIONARIO   BIGINT AUTO_INCREMENT,
    NMFUNCIONARIO   VARCHAR(100),
    NUMATRICULA     BIGINT,
    DATANASCIMENTO  DATE,
    DATACONTRATACAO DATE,
    NMRUA           VARCHAR(100),
    NUMERO          VARCHAR(20),
    COMPLEMENTO     VARCHAR(100),
    BAIRRO          VARCHAR(150),
    CIDADE          VARCHAR(150),
    ESTADO          VARCHAR(100),
    FOTO            BYTE,
    IDEQUIPE        BIGINT NOT NULL,
CONSTRAINT PK_FUNCIONARIO PRIMARY KEY(IDFUNCIONARIO),
CONSTRAINT FK_FUNCIONARIO_EQUIPE FOREIGN KEY(IDEQUIPE) REFERENCES EQUIPE(IDEQUIPE));

CREATE TABLE FERIASFUNCIONARIO(

    IDFERIASFUNCIONARIO BIGINT AUTO_INCREMENT,
    IDFUNCIONARIO       BIGINT,
    DTINICIO            DATE,
    DTTERMINO           DATE,
CONSTRAINT PK_FERIASFUNCIONARIO PRIMARY KEY(FERIASFUNCIONARIO),
CONSTRAINT FK_FERIAS_FUNCIONARIO    FOREIGN KEY(IDFUNCIONARIO) REFERENCES FUNCIONARIO(IDFUNCIONARIO));

)
